    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        if("".equals(id.getText())
            || "".equals(nama.getText())
            || "".equals(alamat.getText())
            || "".equals(jenisBuku.getText())
            || "".equals(judul.getText())
            || "".equals(tglPinjam.getDate())
            || "".equals(tglKembali.getDate())
            || "".equals(status.getText()))
        {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            String SQL = "INSERT INTO tb_buku (ID,Nama,Alamat,JenisBuku,JudulBuku,TanggalPinjam,TanggalKembali,Status)"
            + "VALUES('"
            + id.getText()+"','"
            + nama.getText()+"','"
            + alamat.getText() +"','"
            + jenisBuku.getText()+"','"
            + judul.getText()+"','"
            + tglPinjam.getDate()+"','"
            + tglKembali.getDate()+"','"
            +status.getText()+"')";
            int status = koneksiDB.execute(SQL);
            if(status==1){
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            } else{
                JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_saveActionPerformed

