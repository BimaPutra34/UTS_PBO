private void selectData() {
        String kolom[] = {"ID","Nama","Alamat","JenisBuku","JudulBuku","TanggalPinjam","TanggalKembali","Status"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_buku";
        ResultSet rs = koneksiDB.executeQuery(SQL);
        try{
            while(rs.next()){
                String id = rs.getString(1);
                String nama = rs.getString(2);
                String alamat = rs.getString(3);
                String jnsBuku = rs.getString(4);
                String jdlBuku = rs.getString(5);
                String tglPinjam = rs.getString(6);
                String tglKembali = rs.getString(7);
                String status = rs.getString(8);
                String data[] = {id,nama,alamat,jnsBuku,jdlBuku,tglPinjam,tglKembali,status};
                dtm.addRow(data);
            }
        } catch(SQLException ex){
            Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
        }
        tabel.setModel(dtm);
    }
